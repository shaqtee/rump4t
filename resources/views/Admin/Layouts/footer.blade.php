        function updateTees() {
            const selectedGolfCourse = $('#m_golf_course_id').find(':selected');
            const teeData = selectedGolfCourse.data('tee');

            const tTeeManId = $('#t_tee_man_id');
            const tTeeLadiesId = $('#t_tee_ladies_id');
            const selectedTeeManId = tTeeManId.data('selected');
            const selectedTeeLadiesId = tTeeLadiesId.data('selected');

            tTeeManId.empty().append('<option value="" disabled selected>Select Tee</option>');
            tTeeLadiesId.empty().append('<option value="" disabled selected>Select Tee</option>');

            if (!teeData) {
                tTeeManId.prop('disabled', true);
                tTeeLadiesId.prop('disabled', true);
                return;
            }

            tTeeManId.prop('disabled', false);
            tTeeLadiesId.prop('disabled', false);

            $.each(teeData, function(index, tee) {
                const optionMan = $('<option></option>').val(tee.id).text(tee.tee_type);
                const optionLadies = $('<option></option>').val(tee.id).text(tee.tee_type);

                if (tee.id == selectedTeeManId) {
                    optionMan.prop('selected', true);
                }
                if (tee.id == selectedTeeLadiesId) {
                    optionLadies.prop('selected', true);
                }

                tTeeManId.append(optionMan);
                tTeeLadiesId.append(optionLadies);
            });
        }

        $('#m_golf_course_id').change(updateTees);

        if ($('#m_golf_course_id').val()) {
            updateTees();
        }
    });
